"""Standardized experiment naming, config loading, and artifact logging."""

import json
import yaml
from pathlib import Path
from datetime import datetime

def generate_run_name(method: str, 
                      model: str, 
                      dataset: str
    ) -> str:
    
    '''
    Description:
        Generates a standardized run name for an experiment based on the method, model, and dataset.
        Example:
            method = "finetuning"
            model = "Mistral-7B-v0.3"
            dataset = "CNN/DailyMail"
            Output: "finetuning_mistral7v03_cnndaily_20240601_123456"
    Args:
        method (str): The name of the method or approach used in the experiment (e.g., "finetuning", "prompting")
        model (str): The name of the model being used (e.g., "Mistral-7B-v0.3", "bert-base-uncased").
        dataset (str): The name of the dataset being used (e.g., "CNN/DailyMail", "glue/mnli").
    Returns:
        str: A standardized run name that includes the method, model, dataset, and a timestamp for uniqueness.
    '''
    
    model_short = model.split("/")[-1].lower().replace("-", "").replace(".", "")
    dataset_short = dataset.lower().replace("_", "").replace("/", "")[:8]
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    return f"{method}_{model_short}_{dataset_short}_{timestamp}"

def load_config(config_path: str) -> dict:
    
    '''
    Description:
        Loads a YAML configuration file and returns it as a dictionary.
    Args:
        config_path (str): The file path to the YAML configuration file.
    Returns:
        dict: A dictionary containing the configuration parameters loaded from the YAML file.
    '''
    
    config_path = Path(config_path)
    if not config_path.exists():
        raise FileNotFoundError(f"Config not found: {config_path}")
    with open(config_path, "r") as f:
        return yaml.safe_load(f)
    
def save_metrics(run_name: str, 
                 metrics: dict, 
                 output_dir: str,
                 suffix: str = "metrics",
    ) -> Path:
    
    '''
    Description:
        Saves the evaluation metrics of an experiment to a JSON file in a standardized format.
    Args:
        run_name (str): The name of the experiment run, typically generated by the `generate_run_name` function.
        metrics (dict): A dictionary containing the evaluation metrics to be saved (e.g., {"accuracy": 0.95, "f1_score": 0.92}).
        output_dir (str): The directory where the metrics JSON file will be saved. Default is "outputs/reports". 
        The function will create this directory if it does not exist.
    Returns:
        Path: The file path to the saved metrics JSON file. The file will be named in the format "{run_name}_metrics.json" and 
        will include the run name, timestamp, and the provided metrics.
    '''
    
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)
    out_path = output_dir / f"{run_name}_{suffix}.json"
    with open(out_path, "w") as f:
        json.dump({"run_name": run_name, "timestamp": datetime.now().isoformat(), **metrics}, f, indent=2,)
    return out_path

def save_predictions(run_name: str, 
                     predictions: list[dict], 
                     output_dir: str,
    ) -> Path:
    
    '''
    Description:
        Saves the predictions of an experiment to a JSONL file in a standardized format.
    Args:
        run_name (str): The name of the experiment run, typically generated by the `generate_run_name` function.
        predictions (list[dict]): A list of dictionaries, where each dictionary represents a single prediction 
        (e.g., [{"input": "What is the capital of France?", "prediction": "Paris", "label": "Paris"}, ...]).
        output_dir (str): The directory where the predictions JSONL file will be saved. Default is "outputs/predictions". 
        The function will create this directory if it does not exist.
    Returns:
        Path: The file path to the saved predictions JSONL file. The file will be named in the format "{run_name}_preds.jsonl" 
        and will include the run name, timestamp, and the provided predictions. Each line in the JSONL file will represent a 
        single prediction in JSON format.
    '''
    
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)
    out_path = output_dir / f"{run_name}_preds.jsonl"
    with open(out_path, "w") as f:
        for pred in predictions:
            f.write(json.dumps(pred) + "\n")
    return out_path

def load_metrics(run_name: str, 
                 output_dir: str = "outputs/reports"
    ) -> dict:
    
    '''
    Description:
        Loads the evaluation metrics of an experiment from a JSON file in a standardized format.
    Args:
        run_name (str): The name of the experiment run, typically generated by the `generate_run_name` function.
        output_dir (str): The directory where the metrics JSON file is saved. Default is "outputs/reports". 
        The function will look for a file named in the format "{run_name}_metrics.json" in this directory.
    Returns:
        dict: A dictionary containing the evaluation metrics loaded from the JSON file. The dictionary will include the run name, 
        timestamp, and the metrics that were originally saved using the `save_metrics` function. If the specified metrics file does not 
        exist, the function will raise a FileNotFoundError with a message indicating that the metrics file was not found.
    '''
    
    metrics_path = Path(output_dir) / f"{run_name}_metrics.json"
    if not metrics_path.exists():
        raise FileNotFoundError(f"Metrics not found: {metrics_path}")
    with open(metrics_path, "r") as f:
        return json.load(f)
    
def list_runs(output_dir: str = "outputs/reports") -> list[str]:
    
    '''
    Description:
        Lists all the experiment runs for which metrics have been saved in a specified directory. 
        The function looks for JSON files in the format "{run_name}_metrics.json" and extracts the run names from the file names.
    Args:
        output_dir (str): The directory where the metrics JSON files are saved. Default is "outputs/reports". The function 
        will look for files in this directory that match the pattern "*_metrics.json".
    Returns:
        list[str]: A sorted list of run names for which metrics have been saved. The run names are extracted from the file 
        names by removing the "_metrics" suffix and the ".json" extension. If the specified output directory does not exist, 
        the function will return an empty list.
    '''
    
    output_dir = Path(output_dir)
    if not output_dir.exists():
        return []
    return sorted(p.stem.replace("_metrics", "") for p in output_dir.glob("*_metrics.json"))